org 100h
include 'emu8086.inc'
call SCAN_NUM
MOV AX,CX
MOV BX,AX
START:
    CMP BX,0
    JE END
    CMP BX,1
    JE EXIT
    DEC BX
    MUL BX
    JE EXIT
    JMP START  
END:
   MOV AX,1
   CALL PRINT_NUM
   JMP GO
EXIT:
   CALL PRINT_NUM
GO:
ret 
DEFINE_PRINT_NUM
DEFINE_PRINT_NUM_UNS
DEFINE_SCAN_NUM
